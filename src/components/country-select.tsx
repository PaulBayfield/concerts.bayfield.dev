interface CountrySelectProps {
  value: string
  onChange: (code: string) => void
  placeholder?: string
  className?: string
}

export function CountrySelect({ value, onChange, placeholder = "Select country…", className }: CountrySelectProps) {
  const [open, setOpen] = useState(false)
  const [query, setQuery] = useState("")
  const containerRef = useRef<HTMLDivElement>(null)
  const inputRef = useRef<HTMLInputElement>(null)

  const selected = COUNTRIES.find(c => c.code === value)

  const filtered = query
    ? COUNTRIES.filter(c =>
        c.name.toLowerCase().includes(query.toLowerCase()) ||
        c.code.toLowerCase().includes(query.toLowerCase())
      )
    : COUNTRIES

  useEffect(() => {
    function handleClick(e: MouseEvent) {
      if (containerRef.current && !containerRef.current.contains(e.target as Node)) {
        setOpen(false)
        setQuery("")
      }
    }
    document.addEventListener("mousedown", handleClick)
    return () => document.removeEventListener("mousedown", handleClick)
  }, [])

  const handleOpen = () => {
    setOpen(true)
    setTimeout(() => inputRef.current?.focus(), 0)
  }

  const handleSelect = (code: string) => {
    onChange(code)
    setOpen(false)
    setQuery("")
  }

  const handleClear = (e: React.MouseEvent) => {
    e.stopPropagation()
    onChange("")
    setOpen(false)
    setQuery("")
  }

  return (
    <div ref={containerRef} className={cn("relative", className)}>
      <Button
        type="button"
        variant="outline"
        role="combobox"
        aria-expanded={open}
        className="w-full justify-between h-9 px-3 font-normal"
        onClick={handleOpen}
      >
        <span className="flex items-center gap-2 min-w-0">
          {selected ? (
            <>
              <ReactCountryFlag countryCode={selected.code} svg style={{ width: "1.2em", height: "1.2em" }} />
              <span className="truncate">{selected.name}</span>
            </>
          ) : (
            <span className="text-muted-foreground truncate">{placeholder}</span>
          )}
        </span>
        <span className="flex items-center gap-1 shrink-0">
          {value && (
            <X className="h-3.5 w-3.5 text-muted-foreground hover:text-foreground" onClick={handleClear} />
          )}
          <ChevronsUpDown className="h-4 w-4 text-muted-foreground" />
        </span>
      </Button>

      {open && (
        <div className="absolute top-full mt-1 w-full z-50 rounded-md border bg-popover shadow-lg">
          <div className="p-1.5 border-b">
            <input
              ref={inputRef}
              value={query}
              onChange={e => setQuery(e.target.value)}
              placeholder="Search..."
              className="w-full px-2 py-1 text-sm bg-transparent outline-none placeholder:text-muted-foreground"
            />
          </div>
          <div className="max-h-52 overflow-y-auto py-1">
            {filtered.length === 0 ? (
              <p className="text-sm text-muted-foreground text-center py-3">No results</p>
            ) : (
              filtered.map(country => (
                <button
                  key={country.code}
                  type="button"
                  className={cn(
                    "w-full flex items-center gap-2 px-3 py-1.5 text-sm hover:bg-accent cursor-pointer text-left",
                    value === country.code && "bg-accent/60"
                  )}
                  onClick={() => handleSelect(country.code)}
                >
                  <ReactCountryFlag countryCode={country.code} svg style={{ width: "1.2em", height: "1.2em" }} />
                  <span className="flex-1 truncate">{country.name}</span>
                  {value === country.code && <Check className="h-3.5 w-3.5 shrink-0" />}
                </button>
              ))
            )}
          </div>
        </div>
      )}
    </div>
  )
}
